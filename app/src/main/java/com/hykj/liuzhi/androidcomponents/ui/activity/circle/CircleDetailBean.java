package com.hykj.liuzhi.androidcomponents.ui.activity.circle;

public class CircleDetailBean {

    /**
     * code : 0
     * msg : 对不起，打赏失败，请稍后重试
     * error : 4
     */

    private int code;
    private String msg;
    private int error;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getError() {
        return error;
    }

    public void setError(int error) {
        this.error = error;
    }
}
