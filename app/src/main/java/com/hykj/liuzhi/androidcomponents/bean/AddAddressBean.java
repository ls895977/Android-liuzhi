package com.hykj.liuzhi.androidcomponents.bean;

public class AddAddressBean {

    /**
     * code : 0
     * msg : 地址添加成功！！
     * error : 0
     */
    private int code;
    private String msg;
    private int error;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getError() {
        return error;
    }

    public void setError(int error) {
        this.error = error;
    }
}
